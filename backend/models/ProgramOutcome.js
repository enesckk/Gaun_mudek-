import mongoose from "mongoose";

const ProgramOutcomeSchema = new mongoose.Schema(
  {
    code: {
      type: String,
      required: true,
      unique: true,
    },
    description: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

export default mongoose.model("ProgramOutcome", ProgramOutcomeSchema);

